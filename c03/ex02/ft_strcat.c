#include <stdio.h>

char	*ft_strcat(char *s1, char *s2)
{
	int	i;
	int	k;

	i = 0;
	k = 0;
	while (s1[i])
		i++;
	while (s2[k])
		s1[i++] = s2[k++];
	s1[i] = '\0';
	return (s1);
}
